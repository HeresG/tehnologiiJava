static class Student {
    private String nume, prenume, specializare;
    private int an;
    private double media;
    private boolean bursier;

    public Student(String n, String p, int a, String s, double m, boolean b) {
        nume = n; prenume = p; an = a; specializare = s; media = m; bursier = b;
    }

    public String getNume() { return nume; }
    public String getPrenume() { return prenume; }
    public String getSpecializare() { return specializare; }
    public double getMedia() { return media; }

    @Override
    public String toString() {
        return nume + " " + prenume + " | An: " + an + " | " + specializare +
               " | Media: " + media + " | " + (bursier ? "Bursier" : "Nebursier");
    }
}

