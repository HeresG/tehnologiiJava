    private void adaugaStudent() {
    String n = txtNume.getText().trim();
    String p = txtPrenume.getText().trim();
    int an = rbAn1.isSelected() ? 1 : 2;
    String spec = (String) comboSpecializare.getSelectedItem();
    boolean bursier = chkBursier.isSelected();
    double med = ((Number) spinnerMedia.getValue()).doubleValue();

    if (n.isEmpty() || p.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Completează numele și prenumele!");
        return;
    }

    studenti.add(new Student(n, p, an, spec, med, bursier));
    afiseazaStudenti();
    
     // Resetare câmpuri după adăugare
    txtNume.setText("");
    txtPrenume.setText("");
    rbAn1.setSelected(true);
    comboSpecializare.setSelectedIndex(0);
    chkBursier.setSelected(false);
    spinnerMedia.setValue(0.0);
    txtNume.requestFocus(); 
    
    
    
}

private void sorteazaAlfabetic() {
    studenti.sort(java.util.Comparator.comparing(Student::getSpecializare)
                                     .thenComparing(Student::getNume)
                                     .thenComparing(Student::getPrenume));
    afiseazaStudenti();
}

private void sorteazaDupaMedia() {
    studenti.sort(java.util.Comparator.comparing(Student::getSpecializare)
                                     .thenComparing(java.util.Comparator.comparingDouble(Student::getMedia).reversed()));
    afiseazaStudenti();
}

private void afiseazaStudenti() {
    StringBuilder sb = new StringBuilder();
    for (Student s : studenti) sb.append(s).append("\n");
    textArea.setText(sb.toString());
}

    
    
